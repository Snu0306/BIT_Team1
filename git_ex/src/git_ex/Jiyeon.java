package git_ex;

import java.util.Scanner;

/* <백준 손익분기점>
월드전자는 노트북을 제조하고 판매하는 회사이다. 노트북 판매 대수에 상관없이 매년 임대료, 재산세, 보험료, 급여 등 A만원의 고정 비용이 들며, 
한 대의 노트북을 생산하는 데에는 재료비와 인건비 등 총 B만원의 가변 비용이 든다고 한다.

예를 들어 A=1,000, B=70이라고 하자. 이 경우 노트북을 한 대 생산하는 데는 총 1,070만원이 들며, 열 대 생산하는 데는 총 1,700만원이 든다.

노트북 가격이 C만원으로 책정되었다고 한다. 
일반적으로 생산 대수를 늘려 가다 보면 어느 순간 총 수입(판매비용)이 총 비용(=고정비용+가변비용)보다 많아지게 된다. 
최초로 총 수입이 총 비용보다 많아져 이익이 발생하는 지점을 손익분기점(BREAK-EVEN POINT)이라고 한다.

A, B, C가 주어졌을 때, 손익분기점을 구하는 프로그램을 작성하시오. 

A,B,C 는 콘솔창에서 입력받는다~
 */

public class Jiyeon {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		// a = 고정비용, b=가변비용, c=노트북 가격
		// 한대당 ++totalSales 
		// 총수입(판매대수*노트북가격) >= 총비용(고정+가변*대수) == break-even point
		System.out.println("FIXED COST/VARIABLE EXPENSES/LAPTOP PRICE");
		String str = sc.nextLine();
		String[] values = str.split("/");

		int a = Integer.parseInt(values[0]);
		int b = Integer.parseInt(values[1]);
		int c = Integer.parseInt(values[2]);

		int numberOfSales = 0;

		if (c > b) { //가변비용이 노트북 가격보다 크면 애초에 손익 분기점 존재 ㄴㄴ 
			do {//일단 판매 
				++numberOfSales;
			} while ((numberOfSales * c) <= (a + (b* numberOfSales))); //총비용이 총수입보다 많거나 같을 때 까지 
			System.out.println("BREAK-EVEN-POINT>>\nWHEN NUMBER OF SALES IS "+numberOfSales);
			}
		else {
			System.out.println("-1");
		}
	}
}